# 기본 계산기

def add(a, b):
		return a+b

def subtract(a, b):
		return a-b

def multiply(a, b):
		return a*b

def divide_free(a, b):
		return a / b

def square(a):
		return a * a

def get_Median(a, b):
		return (a + b)/2

def get_Remainder(a, b):
		return a // b

def get_Abs(num):
		if num >= 0:
				return num
		else:
				return -num

def divide_premium(a, b):
		return a / b

def get_Median(a, b):
		return (a + b)/2
		return a / b
